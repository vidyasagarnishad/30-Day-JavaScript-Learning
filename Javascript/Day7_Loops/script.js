// DAY 7: Loops (for, while, do...while)

// for loop: Print numbers 1 to 5
for (let i = 1; i <= 5; i++) {
  console.log("For loop:", i);
}

// while loop: Print even numbers < 10
let j = 2;
while (j < 10) {
  console.log("While loop:", j);
  j += 2;
}

// do...while loop: Print 1 time even if condition is false
let k = 15;
do {
  console.log("Do...while loop:", k);
  k++;
} while (k < 10);
